package BusDetails;

public class busDetails {
	private int busId;
	private String busName;
	private String fromLocation;
	private String toLocation;
	private String journeyDate;
	private int ticketPrice;
	private String travellingTime;
	private int maximumSeats;
	private int availableSeats;
	public int getBusId() {
		return busId;
	}
	public void setBusId(int busId) {
		this.busId = busId;
	}
	public String getBusName() {
		return busName;
	}
	public void setBusName(String busName) {
		this.busName = busName;
	}
	public String getFromLocation() {
		return fromLocation;
	}
	public void setFromLocation(String fromLocation) {
		this.fromLocation = fromLocation;
	}
	public String getToLocation() {
		return toLocation;
	}
	public void setToLocation(String toLocation) {
		this.toLocation = toLocation;
	}
	public String getJourneyDate() {
		return journeyDate;
	}
	public void setJourneyDate(String journeyDate) {
		this.journeyDate = journeyDate;
	}
	public int getTicketPrice() {
		return ticketPrice;
	}
	public void setTicketPrice(int ticketPrice) {
		this.ticketPrice = ticketPrice;
	}
	public String getTravellingTime() {
		return travellingTime;
	}
	public void setTravellingTime(String travellingTime) {
		this.travellingTime = travellingTime;
	}
	public int getMaximumSeats() {
		return maximumSeats;
	}
	public void setMaximumSeats(int maximumSeats) {
		this.maximumSeats = maximumSeats;
	}
	public int getAvailableSeats() {
		return availableSeats;
	}
	public void setAvailableSeats(int availableSeats) {
		this.availableSeats = availableSeats;
	}
	

}
